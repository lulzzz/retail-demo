function toggleFullscreen() {
    const toggleButton = document.getElementById('screen-toggle');
    if (document.webkitFullscreenElement) {
        if (document.webkitExitFullscreen) {
            document.webkitExitFullscreen();
        } else {
            console.log('Fullscreen API is not supported')
        }
    } else {
        const target = document.getElementById('app-container');
        if (target.webkitRequestFullScreen) {
            target.webkitRequestFullScreen();
        } else {
            console.log('Fullscreen API is not supported')
        }
    }
}

(function () {
    const toggleButton = document.getElementById('screen-toggle');
    document.addEventListener('webkitfullscreenchange', function () {
        if (document.webkitFullscreenElement) {
            toggleButton.src = 'images/fullscreen_exit.svg';
        } else {
            toggleButton.src = 'images/fullscreen.svg';
        }
    });

    window.addEventListener('load', function () {
        var trendRef = firebase.database().ref('heatmap/v0');
        trendRef.on('value', function (snapshot) {
            var data = [];
            snapshot.val().forEach(function (element, index, array) {
                data.push({
                    location: new google.maps.LatLng(element.lat, element.lng),
                    weight: element.priceSum
                });
            });
            heatmap.setData(data);
        });
    });
})();